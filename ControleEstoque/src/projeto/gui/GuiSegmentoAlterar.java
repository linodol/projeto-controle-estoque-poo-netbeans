    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {                                           
        // TODO add your handling code here:
        Fachada fachada = new Fachada();
        String fpAlterar = jTAltSegmento.getText();

        try {
            if (fpAlterar.equals("")) {
                JOptionPane.showMessageDialog(null, "Favor digitar uma descri��o");
                jTAltSegmento.setText(null);
            } else {
                int validoString = Integer.parseInt(fpAlterar);
                JOptionPane.showMessageDialog(null, "Favor digitar uma descri��o");
                jTAltSegmento.setText(null);
            }
        } catch (NumberFormatException x) {
            Segmento s = new Segmento();
            int Segmentos_Codigo = Integer.parseInt(jTCodSegmento.getText());
            String mAlt = jTAltSegmento.getText();
            s.setSegmentos_Codigo(Segmentos_Codigo);
            s.setSegmentos_Descricao(mAlt);
            try {
                fachada.alterarSegmento(s);
            } catch (GeralException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
            dispose();

        }
    }                                          

