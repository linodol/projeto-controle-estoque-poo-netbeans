    private void jBSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBSalvarActionPerformed
        // TODO add your handling code here:
        try{
            //Os codigos da realação produto e fornecedor já estão salvos na variavel publica
            //(codFornecedor)codigo do Fornecedor
            //(codProduto)codigo do Produto
            
            //salvar o fornecedor que estiver selecionado
            Fornecedor f = fachada.consultarForRazaoSocial((String) jFornecedorBox.getSelectedItem());
            
            //salvar o produto que estiver selecionado
            Produto p = fachada.consultarProduto((String) jProdutoBox.getSelectedItem());
            
            //CHAMAR O DAO ALTEARAR PRODUTO_FORNECEDOR
            fachada.alterarLigacaoFornecedorProduto(codFornecedor, codProduto,f.getFornecedores_Codigo(),p.getProdutos_Codigo());
            
            JOptionPane.showMessageDialog(null, "Relação Alterada!");
            dispose();
            
        }catch (GeralException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_jBSalvarActionPerformed

