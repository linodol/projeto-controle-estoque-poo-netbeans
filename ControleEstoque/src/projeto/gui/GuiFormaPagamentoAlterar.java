    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {                                           
        // TODO add your handling code here:
        Fachada fachada = new Fachada();
        String fpAlterar = jTAltFormaPagamento.getText();

        try {
            if (fpAlterar.equals("")) {
                JOptionPane.showMessageDialog(null, "Favor digitar uma descrição");
                jTAltFormaPagamento.setText(null);
            } else {
                int validoString = Integer.parseInt(fpAlterar);
                JOptionPane.showMessageDialog(null, "Favor digitar uma descrição");
                jTAltFormaPagamento.setText(null);
            }
        } catch (NumberFormatException x) {
            FormaPagamento fp = new FormaPagamento();
            int FormaPagamentos_Codigo = Integer.parseInt(jTCodFormaPagamento.getText());
            String mAlt = jTAltFormaPagamento.getText();
            fp.setFormaPagamento_Codigo(FormaPagamentos_Codigo);
            fp.setFormaPagamento_Descricao(mAlt);
            try {
                fachada.alterarFormaPagamento(fp);
            } catch (GeralException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
            dispose();

        }
    }                                          

